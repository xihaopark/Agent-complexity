#!/usr/bin/env python3
"""Generate default task_definitions/<workflow_id>.json from finish manifest.

Partitions manifest steps[] order into contiguous tasks (~4 steps per task, 2–8 tasks).
Does not overwrite existing files unless --force.

Run from repo root:
  python3 main/paper_primary_benchmark/tools/generate_task_definitions.py --main-root main
"""

from __future__ import annotations

import argparse
import json
import math
from datetime import date
from pathlib import Path
from typing import Any


def _num_tasks(n: int) -> int:
    if n <= 0:
        return 0
    if n == 1:
        return 1
    # ~4 steps per task, between 2 and 8 tasks
    k = math.ceil(n / 4)
    return min(8, max(2, k))


def _chunk(step_ids: list[str], k: int) -> list[list[str]]:
    n = len(step_ids)
    k = min(max(1, k), n)
    base, rem = divmod(n, k)
    out: list[list[str]] = []
    i = 0
    for t in range(k):
        sz = base + (1 if t < rem else 0)
        out.append(step_ids[i : i + sz])
        i += sz
    return out


def _build_task_doc(
    workflow_id: str,
    manifest: dict[str, Any],
    step_chunks: list[list[str]],
) -> dict[str, Any]:
    steps_by_id = {str(s.get("id")): s for s in manifest.get("steps", []) if s.get("id")}
    tasks: list[dict[str, Any]] = []
    for idx, chunk in enumerate(step_chunks, start=1):
        snapshots = []
        for sid in chunk:
            s = steps_by_id.get(sid, {})
            snapshots.append(
                {
                    "id": sid,
                    "name": str(s.get("name", sid)),
                    "outputs": list(s.get("outputs") or []),
                    "depends_on": list(s.get("depends_on") or []),
                }
            )
        first_o = (snapshots[0].get("outputs") or [None])[0]
        last_o = (snapshots[-1].get("outputs") or [None])[0]
        tasks.append(
            {
                "task_id": f"stage_{idx:02d}",
                "pipeline_stage_label": f"Automated stage {idx} ({len(chunk)} finish steps)",
                "order": idx,
                "step_ids": chunk,
                "metadata": {
                    "autogenerated": True,
                    "summary": "Contiguous partition from finish manifest order; refine labels and IO as needed.",
                },
                "inputs": (
                    [{"role": "upstream", "derived_from_step_ids": [chunk[0]]}]
                    if idx > 1
                    else [{"role": "workflow_entry", "description": "See finish manifest input_hints"}]
                ),
                "outputs": [
                    {
                        "role": "checkpoint_last_step",
                        "path_glob": last_o,
                        "derived_from_step_ids": [chunk[-1]],
                    }
                ]
                if last_o
                else [],
                "step_snapshots": snapshots,
            }
        )
    return {
        "workflow_id": workflow_id,
        "manifest_path": f"finish/{workflow_id}/manifest.json",
        "version": "1",
        "updated_at": date.today().isoformat(),
        "notes": "Autogenerated contiguous partition. Edit pipeline_stage_label / inputs / outputs for paper-facing semantics.",
        "tasks": tasks,
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--main-root", type=Path, required=True, help="Path to main/")
    ap.add_argument("--workflow-id", type=str, default="", help="Only this workflow")
    ap.add_argument("--force", action="store_true", help="Overwrite existing JSON")
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()
    main_root = args.main_root.resolve()
    bench = main_root / "paper_primary_benchmark"
    index_path = bench / "manifest.json"
    td = bench / "task_definitions"

    with index_path.open(encoding="utf-8") as f:
        index = json.load(f)
    ids = [w["id"] for w in index.get("main_workflows", [])]
    if args.workflow_id:
        ids = [args.workflow_id]

    written = 0
    for wid in ids:
        out_path = td / f"{wid}.json"
        if out_path.is_file() and not args.force:
            continue

        mpath = main_root / "finish" / wid / "manifest.json"
        if not mpath.is_file():
            print(f"SKIP missing manifest {mpath}")
            continue
        with mpath.open(encoding="utf-8") as f:
            manifest = json.load(f)
        step_ids = [str(s.get("id")) for s in manifest.get("steps", []) if s.get("id")]
        if not step_ids:
            print(f"SKIP no steps {wid}")
            continue
        k = _num_tasks(len(step_ids))
        chunks = _chunk(step_ids, k)
        doc = _build_task_doc(wid, manifest, chunks)
        if args.dry_run:
            print(wid, "->", len(chunks), "tasks", [len(c) for c in chunks])
            written += 1
            continue
        td.mkdir(parents=True, exist_ok=True)
        with out_path.open("w", encoding="utf-8") as f:
            json.dump(doc, f, indent=2, ensure_ascii=False)
            f.write("\n")
        print(f"Wrote {out_path}")
        written += 1

    if args.dry_run:
        print(f"dry-run: would write {written} files")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
